const billOfLading = () => {
  let x = document.getElementById("top_details");
  if (x.style.display === "none") {
    x.style.display = "block";
  } else {
    x.style.display = "none";
  }
}

const containerHide = () => {
  let x = document.getElementById("container_details");
  if (x.style.display === "none") {
    x.style.display = "block";
  } else {
    x.style.display = "none";
  }
}

 